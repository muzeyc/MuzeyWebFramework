package com.muzey.web.model;

public class DMBasicInfoModel {

	private int id;
	private String name;
	private String province;
	private String city;
	private String dmdistrict;
	private String address;
	private int lv;
	private String state;
	private String tel;
	private String dmdesc;
	private Integer pictureid;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDmdistrict() {
		return dmdistrict;
	}

	public void setDmdistrict(String dmdistrict) {
		this.dmdistrict = dmdistrict;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getLv() {
		return lv;
	}

	public void setLv(int lv) {
		this.lv = lv;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getDmdesc() {
		return dmdesc;
	}

	public void setDmdesc(String dmdesc) {
		this.dmdesc = dmdesc;
	}

	public Integer getPictureid() {
		return pictureid;
	}

	public void setPictureid(Integer pictureid) {
		this.pictureid = pictureid;
	}

}
