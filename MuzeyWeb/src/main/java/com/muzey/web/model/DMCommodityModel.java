package com.muzey.web.model;

public class DMCommodityModel {

	private int dmid;
	private String dmidName;
	private int commodityid;
	private String commodityNmae;
	private String dmclassify;

	public int getDmid() {
		return dmid;
	}

	public void setDmid(int dmid) {
		this.dmid = dmid;
	}

	public String getDmidName() {
		return dmidName;
	}

	public void setDmidName(String dmidName) {
		this.dmidName = dmidName;
	}

	public int getCommodityid() {
		return commodityid;
	}

	public void setCommodityid(int commodityid) {
		this.commodityid = commodityid;
	}

	public String getCommodityNmae() {
		return commodityNmae;
	}

	public void setCommodityNmae(String commodityNmae) {
		this.commodityNmae = commodityNmae;
	}

	public String getDmclassify() {
		return dmclassify;
	}

	public void setDmclassify(String dmclassify) {
		this.dmclassify = dmclassify;
	}

}
